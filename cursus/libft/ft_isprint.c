int ft_isprint(int c)
{
    if ((c >= 40 && c <= 57) || (c >= 60 && c <= 77) || (c >= 100 && c <= 137)\
    || (c >= 140 && c <= 176))
        return (1);
    return (0);
} 

#include <stdio.h>
int main(void)
{
    int c = '|';
   int result = ft_isprint(c);
    printf("result: %d\n",result);
    return (0);
}